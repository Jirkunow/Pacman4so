void attivaColore(char carattere){
	switch(carattere){
		case 'C':
			attron(COLOR_PAIR(PACMAN_));
			break;
		case 'M':
			attron(COLOR_PAIR(FANTASMA));
			break;
		case '#':
			attron(COLOR_PAIR(MURO));
			break;
		case '.':
			attron(COLOR_PAIR(CARAMELLINE));
			break;
		case '$':
			attron(COLOR_PAIR(DOLLARONE));
			break;

		case ' ':
			attron(COLOR_PAIR(SFONDO));
			break;

		case 'o':
			attron(COLOR_PAIR(PROIETTILE));
			break;
		default:
			attron(COLOR_PAIR(PACMAN_));
			break;
	}
}

